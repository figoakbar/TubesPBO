public abstract class Person {
    private String Nama,NIK;

    public Person(String Nama, String NIK) {
        this.Nama = Nama;
        this.NIK = NIK;
    }

    public String getNama() {
        return Nama;
    }

    public void setNama(String Nama) {
        this.Nama = Nama;
    }

    public String getNIK() {
        return NIK;
    }

    public void setNIK(String NIK) {
        this.NIK = NIK;
    }

}
